export type TranslationKey = keyof typeof translations.ru;

export function isAppLanguage(value: unknown): value is AppLanguage {
  return typeof value === 'string' && LANGUAGES.includes(value as AppLanguage);
}

export function t(language: AppLanguage, key: string): string {
  const table = translations[language] as Record<string, string>;
  const fallback = translations.ru as Record<string, string>;
  return table[key] ?? fallback[key] ?? key;
}
